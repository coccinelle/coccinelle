let rec sexp_of_name =
  function
  | RegularName v1 ->
      let v1 = sexp_of_wrap Conv.sexp_of_string v1
      in Sexp.List [ Sexp.Atom "RegularName"; v1 ]
  | CppConcatenatedName v1 ->
