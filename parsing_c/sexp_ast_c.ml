  and v2 =
    match v2 with
    | (v1, v2) ->
        let v1 = Conv.sexp_of_list (sexp_of_wrap2 sexp_of_parameterType) v1
        and v2 = sexp_of_wrap Conv.sexp_of_bool v2
        in Sexp.List [ v1; v2 ]
  in Sexp.List [ v1; v2 ]
